package designPrincinple;

/**
 *  7种设计原则，分别为开闭原则、里氏替换原则、依赖倒置原则、单一职责原则、接口隔离原则、迪米特法则和合成复用原则
 *  这7种设计原则是软件设计模式必须尽量遵循的原则，各种原则要求的侧重点不同。
 *  开闭原则是总纲，它告诉我们要对扩展开放，对修改关闭；
 *  里氏替换原则告诉我们不要破坏继承体系；依赖倒置原则告诉我们要面向接口编程；
 *  单一职责原则告诉我们实现类要职责单一；
 *  接口隔离原则告诉我们在设计接口的时候要精简单一；
 *  迪米特法则告诉我们要降低耦合度；
 *  合成复用原则告诉我们要优先使用组合或者聚合关系复用，少用继承关系复用。
 */